
public class Triple {
	private String first;
	private String second;
	private String third;
	
	public Triple (String input1, String input2, String input3) {
		first = input1;
		second = input2;
		third = input3;
	}
	
	public String getFirst() {
		return first;
	}
	
	public String getSecond() {
		return second;
	}
	
	public String getThird() {
		return third;
	}
}
